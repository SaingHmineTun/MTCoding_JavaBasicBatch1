package com.saimao;

public class Lesson25_ForLoop {
    public static void main(String[] args) {


        /*
        1. Initial Value
        2. Boolean Expression using that value
        3. Update that value
        Syntax : for (1;2;3)
         */
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            System.out.println(ch);
        }
    }
}
